def parse(art: str) -> bytes:
    rows = [line for line in art.splitlines() if line]
    assert len(rows) == 8, f"glyph must have 8 rows, got {len(rows)}"
    out = bytearray(8)
    for y, row in enumerate(rows):
        row = row.ljust(8, ".")
        b = 0
        for x in range(8):
            if row[x] == "#":
                b |= 1 << (7 - x)
        out[y] = b
    return bytes(out)


def main() -> None:
    glyphs = {ch: parse(art) for ch, art in GLYPHS.items()}
    out = bytearray(256 * 16)
    for ch, bitmap in glyphs.items():
        code = ord(ch)
        out[code * 16:code * 16 + 8] = bitmap        # plane 0 = glyph
        # plane 1 stays 0 -> color index 1 (white) everywhere a bit is set
    dst = Path(__file__).resolve().parent / "splash_font.bin"
    dst.write_bytes(out)
    print(f"wrote {dst.name} ({len(out)} bytes)")


if __name__ == "__main__":
    main()
